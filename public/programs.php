        <section class='content'>
         <h2>Program works</h2>
          <p>Officials play a vital role in the game, they are often described as the third team on the ice. The basic role of the on-ice
officials can be broken down into two simple words – safe and fair. For a referee to view and officiate the game with
these two words in mind, they should be able to call a game that is acceptable to all of the participants.
The LRG training and development program is designed to assist young officials learn their role in the game and develop the skills necessary to ensure a safe and fair hockey game. This program consistently proves to be instrumental with
guiding young officials for improvement of</p>

<ol>
  <p>Decision making skills</p>
  <p>On ice awareness as an official</p>
  <p>Communication skills</p>
  <p>Applying the rules of the game to promote safe and fair play</p>
</ol>
<p>As part of this program we assign experienced officials as mentors to assist with the training and develop our younger
officials. The on-ice mentorship and off-ice guidance brings practical and classroom style learning together with positive enforcement in order to build their self-confidence</p>

</section>

