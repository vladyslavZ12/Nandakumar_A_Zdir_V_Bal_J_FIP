          <p>In 2007, the London Referee Group, also known as the LRG, was established. This was the
merger of the Forest City Referees Association (FCRA) and the London Hockey Referees Association (LHRA).
London Referees Group is a non-profit membership-based organization that acts as a resource for hockey officials in the City of London and surrounding area. LRG is an official
partner of:</p>
            <ul>
              <p>· Ontario Hockey Federation</p>
              <p>· Hockey Canada</p>
              <p>· Alliance Hockey</p>
              <p>· Ontario Minor Hockey Association</p>
              <p>· Ontario Women’s Hockey Association</p>
              <p>· Sledge Hockey</p>
            </ul>

            <p>The LRG provides on ice officials for games at various levels of hockey, such as: minor
hockey, high school, sledge, and adult. As an organization we strive to maintain an environment that embodies the core values of integrity, leadership, perseverance, respect and
teamwork while enhancing self-esteem, promoting self-confidence and cultivating a lifelong
passion for the game.
LRG members commit to representing the vision and mission of the London Referees Group.
They encourage a high standard of ethics, closer cooperation, and understanding of the role
of the referee among officials, coaches, players, parents, the community and the media.
Membership requires a one-time application and statement of interest. Membership is open
to all genders, ages 14 and up.
As an organization, we are focused on developing and maintaining a membership consisting
of experienced and capable on ice officials. They are advocates for the ideals of good
sportsmanship and fair play through qualified officiating in hockey and respect for the authority of hockey officials at all levels of competition.
The executive leadership of the LRG supports ongoing development of its members by promotion of education and training programs that advance the skills of hockey officials. As
well, as providing information programs to coaches, parents, and community members that
advocate for a stronger understanding for the role of the on-ice officials and the competence of these individuals.</p>
